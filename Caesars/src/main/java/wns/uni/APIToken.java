package wns.uni;

public class APIToken {

	   private String valid;
	   private String token;

	   public String getValid() {
	      return this.valid;
	   }

	   public void setValid(String valid) {
	      this.valid = valid;
	   }

	   public String getToken() {
	      return this.token;
	   }

	   public void setToken(String token) {
	      this.token = token;
	   }

}
